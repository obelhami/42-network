int	ft_iterative_factorial(int nb)
{
	int	f;
	int	i;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		f = 1;
	else
		f = 1;
	i = 2;
	while (i <= nb)
	{
		f = f * i;
		i++;
	}
	return (f);
}
